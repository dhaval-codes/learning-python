# Modifying Strings

# upper case
a = "hello world"
print(a.upper())

#lower case
b = "HEllo WOrLd"
print(b.lower())

#removes white spaces
c = "Hello Word !  "
print(c.strip())

#replace string
d = "Hello World!"
print(d.replace("W","D"))

#split string
e = "Hello Mr Beast"
print(e.split(" ")) # splits from the empty spaces
